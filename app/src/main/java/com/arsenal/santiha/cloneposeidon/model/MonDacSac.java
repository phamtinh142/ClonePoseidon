package com.arsenal.santiha.cloneposeidon.model;

public class MonDacSac {
    private String ten;
    private String hinh;

    public MonDacSac(String ten, String hinh) {
        this.ten = ten;
        this.hinh = hinh;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getHinh() {
        return hinh;
    }

    public void setHinh(String hinh) {
        this.hinh = hinh;
    }
}
